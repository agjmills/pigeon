import { Hono } from 'hono'
import type { AppEnv } from '../types'
import { markMessageOpened } from '../lib/db'

export const trackingRoutes = new Hono<AppEnv>()

// 1x1 transparent GIF
const PIXEL = new Uint8Array([
  0x47,0x49,0x46,0x38,0x39,0x61,0x01,0x00,0x01,0x00,0x80,0x00,0x00,
  0xff,0xff,0xff,0x00,0x00,0x00,0x21,0xf9,0x04,0x00,0x00,0x00,0x00,
  0x00,0x2c,0x00,0x00,0x00,0x00,0x01,0x00,0x01,0x00,0x00,0x02,0x02,
  0x44,0x01,0x00,0x3b,
])

trackingRoutes.get('/:token', async (c) => {
  const token = c.req.param('token')
  c.executionCtx.waitUntil(markMessageOpened(c.env.DB, token))
  return new Response(PIXEL, {
    headers: {
      'Content-Type': 'image/gif',
      'Cache-Control': 'no-store, no-cache, must-revalidate',
    },
  })
})
